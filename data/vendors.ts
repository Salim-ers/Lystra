export function getVendor(slug: string) {
  return VENDORS.find((v) => v.slug === slug);
}
export function getFeaturedVendors(limit = 6) {
  return VENDORS.filter((v) => v.isFeatured).slice(0, limit);
}
export function getVendorsByCategory(slug: string) {
  return VENDORS.filter((v) => v.category === slug || v.secondaryCategories?.includes(slug));
}
export function getVendorsByFamily(family: FamilySlug) {
  return VENDORS.filter((v) => v.family === family);
}
export function getVendorsByCity(city: string) {
  return VENDORS.filter((v) => v.city.toLowerCase() === city.toLowerCase());
}
export function getVendorsByEvent(eventLabel: string) {
  return VENDORS.filter((v) => (v.eventTypes ?? []).includes(eventLabel));
}
export function getSimilarVendors(vendor: Vendor, limit = 3) {
  return VENDORS
    .filter((v) => v.slug !== vendor.slug && (v.category === vendor.category || v.family === vendor.family))
    .sort((a, b) => b.averageRating - a.averageRating)
    .slice(0, limit);
}
